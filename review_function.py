def dot(u,v):
    p = 0
    for i in range(len(u)):
        p += u[i] * v[i]
    print(p)
    return p # p ตัวนี้จะถูกส่งเข้าไปเก็บไว้ที่ d1

a = [1,2,3]
b = [4,5,6]
d1 = dot(a,b) # ตรงนีี้ คือ ได้รับค่าจาก p ที่ return มาให้
print(2*dot([1,2,3], b))  # ทำได้แต่ไม่ควรทำ มันแปลก คืนresult ออกมาก็น่าจะไปเก็บไว้หน่อยสักที่นึง เช่น ในตัวแปร
print(d1)
